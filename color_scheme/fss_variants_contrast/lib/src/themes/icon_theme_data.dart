import 'package:flutter/material.dart';

/// Function to output the iconTheme
/// 
/// @author Fredrick Allan Grott
IconThemeData iconThemeData(Color onSurface) => IconThemeData(
      size: 24,
      fill: 0.0,
      weight: 400,
      grade: 0.0,
      opticalSize: 24.0,
      color: onSurface,
      opacity: 1.0,
      applyTextScaling: false,
    );
